#2) მომხმარებელს შემოატანინეთ რიცხვი შემდეგ while loop ის გამოყენებით 1-დან მომხმარების შემოტანილ რიცხვამდე დაბეჭდეთ ყველა რიცხვი და თან გვერძე მიუწერეთ ლუწია თუ კენტი

user_num = int(input ("enter num"))   
i = 0 
while i < user_num:
    if i %2 ==0:
        print(str(i)+"ლუწია")
    else:
        print(str(i)+"კენტია")
i = i+ 1          