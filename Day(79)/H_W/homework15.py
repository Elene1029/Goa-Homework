# 15) შექმენით 2 ცვლადი სადაც მომხმარებლის შემოტანილ რიცხვებს იმახსოვრებთ, შემდეგ კი შეადარეთ ისინი ერთმანეთს.

num1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = int(input("შეიყვანეთ მეორე რიცხვი: "))

if num1 > num2:
    print(str(num1) + " მეტია ვიდრე " + str(num2))
elif num1 < num2:
    print(str(num1) + " ნაკლებია ვიდრე " + str(num2))
else:
    print("ორივე რიცხვი ტოლია.")