# 14) ყველა მათემატიკურ ოპერატორებზე (+, -, /, *, //, %) გააკეთეთ 5-5 მაგალითი და დაპრინტეთ.

# შეკრება (+)
print(9 + 3)
print(40 + 10)
print(-9 + 40)
print(7.3 + 2.5)
print(150 + 3)

# გამოკლება (-)
print(30 - 5)
print(10 - 20)
print(-5 - 5)
print(3.5 - 1.5)
print(190 - 99)

# გაყოფა (/)
print(10 / 2)
print(50 / 5)
print(7 / 2)
print(15.0 / 3.0)
print(100 / 4)

# გამრავლება (*)
print(5 * 2)
print(10 * 3)
print(-5 * 3)
print(3.5 * 2)
print(100 * 0)

# მთელზე გაყოფა (//)
print(10 // 3)
print(50 // 7)
print(15 // 4)
print(-8 // 3)
print(100 // 6)

# ნაშთიანი გაყოფა (%)
print(10 % 3)
print(50 % 7)
print(15 % 4)
print(-8 % 3)
print(100 % 6)
